package cursologicacapitulo03;


public class TiposPrimitivos {

	public static void main(String[] args) {
		
		Byte variavelByte = null;
		System.out.println("Variável Byte: " + variavelByte);
		System.out.println("------------------------------");
		
		Short variavelShort = 1;
		System.out.println("Variável Short: " + variavelShort);
		System.out.println("------------------------------");
		
		Integer variavelInteger = 1;
		System.out.println("Variável Integer: " + variavelInteger);
		System.out.println("------------------------------");
		
		Long variavelLong = 1L;
		System.out.println("Variável Long: " + variavelLong);
		System.out.println("------------------------------");
		
		Float variavelFloat = 1.0f;
		System.out.println("Variável Float: " + variavelFloat);
		System.out.println("------------------------------");
		
		Double variavelDouble = 1.0;
		System.out.println("Variável Double: " + variavelDouble);
		System.out.println("------------------------------");
		
		Character variavelCharacter = 'A';
		System.out.println("Variável Character: " + variavelCharacter);
		System.out.println("------------------------------");
		
		Boolean variavelBoolean = true;
		System.out.println("Variável Boolean: " + variavelBoolean);
		System.out.println("------------------------------");
		
		

	}

}
